#!/usr/bin/env node

import readline from 'readline/promises';
import { stdin as input, stdout as output } from 'process';
import fs from 'fs/promises';
import path from 'path';
import { GoogleGenAI } from '@google/genai';
import { config } from 'dotenv';

// Load environment variables for local testing
config();

const rl = readline.createInterface({ input, output });

async function main() {
  console.log('\x1b[36m%s\x1b[0m', '🚀 مرحباً بك في QAMANIAI CLI - منصة البناء الذكي');
  
  try {
    const projectName = await rl.question('\x1b[33m? ما اسم مشروعك؟ \x1b[0m');
    const userPrompt = await rl.question('\x1b[33m? ماذا تريد أن تبني (صف صفحتك)؟ \x1b[0m');

    console.log('\n\x1b[34mجاري تحضير ملفات المشروع: ' + projectName + '...\x1b[0m');

    const projectDir = path.resolve(process.cwd(), projectName);
    
    // Create project directory
    await fs.mkdir(projectDir, { recursive: true });

    // AI Generation
    const apiKey = process.env.GEMINI_API_KEY || process.env.GOOGLE_API_KEY;
    if (!apiKey) {
      console.log('\x1b[31mخطأ: تفشل العملية بسبب فقدان مفتاح Gemini API. تأكد من إعداد GEMINI_API_KEY.\x1b[0m');
      rl.close();
      return;
    }

    console.log('\x1b[35mجاري استدعاء Gemini AI لتوليد الكود...\x1b[0m');
    
    const genAI = new GoogleGenAI(apiKey);
    const model = genAI.getGenerativeModel({ model: "gemini-1.5-flash" });

    const systemPrompt = `
      You are an expert web developer. Generate a complete, modern, and beautiful single-page website using HTML and Tailwind CSS CDN.
      The theme is: ${userPrompt}.
      Include:
      - A responsive Navbar
      - Hero section with animations
      - Features section
      - Modern typography and color palette
      - RTL support if the user prompt is in Arabic.
      
      Return ONLY the raw HTML code without any markdown formatting wrappers or backticks.
    `;

    const result = await model.generateContent(systemPrompt);
    const generatedHtml = result.response.text().replace(/```html/g, '').replace(/```/g, '').trim();

    // Write files
    await fs.writeFile(path.join(projectDir, 'index.html'), generatedHtml);
    
    // Basic package.json for the new project
    const demoPackage = {
      name: projectName,
      version: "1.0.0",
      description: "Generated by QAMANIAI CLI",
      scripts: {
        "start": "npx serve ."
      }
    };
    await fs.writeFile(path.join(projectDir, 'package.json'), JSON.stringify(demoPackage, null, 2));

    console.log('\n\x1b[32m✅ تم بنجاح! تم إنشاء مشروعك في المجلد: ' + projectName + '\x1b[0m');
    console.log('\x1b[32mللتشغيل:\x1b[0m');
    console.log(`\x1b[37m  cd ${projectName}\x1b[0m`);
    console.log(`\x1b[37m  npm start\x1b[0m`);

  } catch (error) {
    console.error('\x1b[31mحدث خطأ أثناء التشغيل:\x1b[0m', error);
  } finally {
    rl.close();
  }
}

main();
